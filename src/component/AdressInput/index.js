import { Input, Steps } from "antd";
import React, { useEffect, useRef, useState } from "react";
import styles from "./index.module.less";
import { EnvironmentOutlined } from "@ant-design/icons";

const { Step } = Steps;

export default (props) => {
	const [visible, setVisible] = useState();
	const [options, setOptions] = useState([]);
	const [selected, setSelected] = useState();
	const [value, setValue] = useState();

	const myRef = useRef();

	const onChange = (e) => {
		if (e.target.value) {
			setOptions(data);
			setVisible(true);
		} else {
			setOptions([]);
			setVisible(false);
		}
	};

	const onClick = (item) => {
		console.info("onClick=>", item);
		setSelected(item.hotPointID);
		setValue(item.name);
		setVisible(false);
	};

	const onInputClick = () => {
		setVisible(true);
	};

	const description = "This is a description.";

	useEffect(() => {
		document.addEventListener("click", handleDocumentClick);

		return () => document.removeEventListener("click", handleDocumentClick);
	}, []);

	const handleDocumentClick = (e) => {
		const componentNode = myRef.current; // 这里假设组件有一个最外层容器的ref
		if (componentNode && !componentNode.contains(e.target)) {
			// 点击事件发生在组件外部
			// 在这里执行你的处理逻辑
			console.log("点击事件发生在组件外部=>");
		}
	};

	return (
		<div>
			<div ref={myRef} className={styles.layout}>
				<Input
					value={value}
					onChange={onChange}
					onClick={onInputClick}
					prefix={<i className={styles.dot} />}
					suffix={<EnvironmentOutlined />}
				/>
				<div className={styles.mount}>
					<ul className={visible ? "" : styles.close}>
						{options.map((item) => (
							<li
								key={item.lonlat}
								className={item.hotPointID === selected ? styles.selected : ""}
								onClick={() => onClick(item)}
							>
								<div>{item.name}</div>
								<div className={styles.address}>{item.address}</div>
							</li>
						))}
					</ul>
				</div>
			</div>
			<Steps direction="vertical" size="small" className={styles.stepsBox}>
				{list.map((item, index) => (
					<Step
						key={index}
						title={item.instruction}
						icon={
							<i className={styles.order}>
								<div>{index}</div>
							</i>
						}
					/>
				))}
			</Steps>
		</div>
	);
};

